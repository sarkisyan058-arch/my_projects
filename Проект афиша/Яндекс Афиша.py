# * <details> <summary> <span style='color:blue' > Поговорим подробнее про расчет среднего.  </span> </summary>
#     
#     Когда нам нужно провести агрегацию через несколько уровней (к примеру, наш случай, сначала до билета в рамках заказа, затем до сегментов), то на этапе второй агрегации у нас появляется два пути - посчитать общее среднее или среднее от средних. **Среднее от средних** подразумевает, что мы берем уже готовые средние показатели первого этапа агрегации (по каждому заказу) и усредняем их внутри новых групп, тем самым как бы оценивая выручку со стоимости билета, которую готовы заплатить пользовали в **среднестатистическом заказе**. При интерпретации речь, разумеется, тоже должна идти о заказах. В свою очередь **общее среднее** подразумевает - раз мы считаем среднюю выручку со стоимости билета, то и работать мы должны с билетами - в числителе берем суммарную выручку, в знаменателе берем суммарное количество купленных билетов. В этом случае мы уже оцениваем величину выручки от продажи **среднестатистического билета**. Результаты по этим двум подходам будут отличаться. Причем, чем разнороднее наши заказы по объему билетов и затрат, тем существеннее будут разниться результаты этих двух подходов, поскольку при расчете среднего от средних мы уравниваем вклад каждого заказа, влияние экстремальных заказов с большим количеством билетов на показатель снижается (в общем среднем они продавливают показатели своей кучей билетов), а влияние пользователей с заказов количеством билетов, напротив, нарастает (в общем среднем они теряются среди остальных, задавливаются).
# 
#     Но, несмотря на то, что среднее от средних в целом можно еще как-то проинтерпретировать, по факту его практически не используют и, говоря про среднее, подразумевают общее среднее, оно гораздо понятнее. Через сколь много уровней мы бы его не протаскивали и какими бы сильными не были бы различия в объемах групп, это будет стабильная в интерпретации величина - среднее на 1 билет. В свою очередь среднее от средних при неравенстве объема групп может давать искажение уже на первом шаге, а если мы начинаем его использовать многократно (сначала при расчете заказных средних, потом на основе них для общезаказного показателя по сегментам / дням, потом еще по по более верхнеуровневым сегментам), то оно станет вовсе неитерпретируемым, основание у такого среднего многоуровневое, что довольно сложно осмыслить, возникает много "если бы".
